package com.ducnh.stickerstory;

/**
 *
 * @author ducnh
 */
public class Sticker {
    private int x;
    private int y;
    private String image;
    
    public Sticker() {
    }
    
    public int getX() {
        return this.x;
    }
    
    public void setX(int x) {
        this.x = x;
    }
    
    public int getY() {
        return this.y;
    }
    
    public void setY(int y) {
        this.y = y;
    }
    
    public String getImage() {
        return this.image;
    }
    
    public void setImage(String image) {
        this.image = image;
    }
}
